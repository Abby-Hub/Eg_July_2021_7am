package com.pack;

public class Student {
	public int id;
	public String name;
	public int marks;
	
	public Student(){
		System.out.println("In Student Default Constructor...");
	}

	
	public Student(int id, String name, int marks) {
		this.id = id;
		this.name = name;
		this.marks = marks;
	}
	
	
	
	
	

}
